    public function register(Request $request){
        $request->validate([
            'email'=>'unique:users|email',
            'password'=>'min:8',
        ]);
        $user = new User;
        $user->email=$request->email;
        $user->password=$request->password;
        $user->save();
        $user->syncRoles("1");

        toastr()->success('Kayıt Başarılı');
        return redirect()->route('page.login');

    }

